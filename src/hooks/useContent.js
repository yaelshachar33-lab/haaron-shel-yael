export function useContent() {
  const [content, setContent] = useState(DEFAULT_CONTENT)
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    const fetch = async () => {
      try {
        const snap = await getDoc(doc(db, 'config', 'content'))
        if (snap.exists()) {
          setContent({ ...DEFAULT_CONTENT, ...snap.data() })
        }
      } catch (e) {
        console.error(e)
      } finally {
        setLoading(false)
      }
    }
    fetch()
  }, [])

  const updateContent = async (updates) => {
    const next = { ...content, ...updates }
    setContent(next)
    try {
      await setDoc(doc(db, 'config', 'content'), next)
    } catch (e) {
      console.error('useContent setDoc error:', e)
      throw e
    }
  }

  return { content, loading, updateContent }
}
